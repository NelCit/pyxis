#!/usr/bin/env python3
"""Pyxis perf-history dashboard (M10 — plan §35 / §36.6).

Renders a single self-contained HTML page from one or more KPI CSVs
emitted by ``_tools/run_regression.py``. The page is published as a
CI artifact each nightly run (§36.6 "Dashboard: a static HTML page
generated by _tools/perf_dashboard.py from the rolling CSV; published
as a CI artifact each nightly run. No external dashboard service v1.").

Usage:

    python perf_dashboard.py --csv tests/regression/m8_world_lobby/kpis.csv \\
                             --out perf_dashboard.html

    # Multiple fixtures collapsed into one tabbed page:
    python perf_dashboard.py --csv tests/regression/lobby/kpis.csv \\
                             --csv tests/regression/bistro/kpis.csv \\
                             --out perf_dashboard.html

Design rules:

    * **No external assets** — pure inline HTML + CSS + JS. The CI
      job uploads the .html file and viewers open it directly.
    * **No Plotly / D3 / etc.** — uses Chart.js loaded from
      ``cdn.jsdelivr.net`` (offline access is acceptable to render
      without charts; the data table still works in any browser).
    * **No heavy Python deps** — only ``csv`` + standard library, so
      Phase D runs in the same env as Phase B.
"""

from __future__ import annotations

import argparse
import csv
import datetime
import html
import json
import sys
from pathlib import Path
from typing import Any


# KPIs to chart on the dashboard. Tuple = (column, label, unit).
_TRACKED_KPIS: list[tuple[str, str, str]] = [
    ('pathtrace_gpu_p50_ms',         'pass.PathTrace p50',         'ms'),
    ('pathtrace_gpu_p99_ms',         'pass.PathTrace p99',         'ms'),
    ('commit_resources_cpu_p50_ms',  'commitResources p50',        'ms'),
    ('commit_resources_cpu_p99_ms',  'commitResources p99',        'ms'),
    ('frame_cpu_p50_ms',             'render.frame.cpu p50',       'ms'),
    ('frame_cpu_p99_ms',             'render.frame.cpu p99',       'ms'),
    ('wall_clock_ms',                'time-to-first-image (wall)', 'ms'),
    ('rmse',                         'image RMSE vs baseline',     ''),
]


def _read_csv(csv_path: Path) -> list[dict[str, str]]:
    with csv_path.open('r', newline='', encoding='utf-8-sig') as fh:
        return list(csv.DictReader(fh))


def _series_for(rows: list[dict[str, str]], column: str) -> list[dict[str, Any]]:
    """Build a chart-ready list of {x: timestamp, y: float} points,
    dropping rows where the metric isn't populated."""
    out: list[dict[str, Any]] = []
    for row in rows:
        raw = row.get(column, '')
        if raw is None or raw == '':
            continue
        try:
            value = float(raw)
        except (TypeError, ValueError):
            continue
        out.append({
            'x': row.get('timestamp_iso', ''),
            'y': value,
            'status': row.get('status', ''),
        })
    return out


def _fixture_payload(csv_path: Path) -> dict[str, Any]:
    rows = _read_csv(csv_path)
    fixture_name = rows[-1]['fixture'] if rows else csv_path.stem
    return {
        'name': fixture_name,
        'csv':  str(csv_path),
        'rows': len(rows),
        'latest_pyxis': rows[-1].get('pyxis_version', '') if rows else '',
        'latest_gpu':   rows[-1].get('gpu_name', '') if rows else '',
        'latest_status': rows[-1].get('status', '') if rows else '',
        'latest_timestamp': rows[-1].get('timestamp_iso', '') if rows else '',
        'series': {
            column: _series_for(rows, column)
            for column, _label, _unit in _TRACKED_KPIS
        },
        'rows_table': rows[-20:],  # last 20 runs in the bottom table
    }


