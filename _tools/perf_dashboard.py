def _render(payloads: list[dict[str, Any]]) -> str:
    tabs_html = ''.join(
        f'<div class="tab">{html.escape(p["name"])}</div>'
        for p in payloads
    )

    fixture_blocks = []
    for f_idx, payload in enumerate(payloads):
        charts = []
        for column, label, unit in _TRACKED_KPIS:
            unit_suffix = f' ({unit})' if unit else ''
            chart_id = f'chart_{f_idx}_{column}'
            has_data = bool(payload['series'].get(column))
            if has_data:
                body = f'<canvas id="{chart_id}" class="chart-canvas"></canvas>'
            else:
                body = '<div class="no-chart">no samples yet</div>'
            charts.append(
                f'<div class="chart-card">'
                f'<div class="chart-title">{html.escape(label)}{unit_suffix}</div>'
                f'{body}'
                f'</div>'
            )

        table_rows = []
        for row in payload['rows_table']:
            status = row.get('status', '')
            status_class = ('status-pass' if status == 'pass'
                            else 'status-fail' if status == 'fail'
                            else 'status-other')
            cells = [
                row.get('timestamp_iso', ''),
                f'<span class="{status_class}">{html.escape(status)}</span>',
                row.get('pyxis_git_sha', '')[:10],
                row.get('pathtrace_gpu_p50_ms', ''),
                row.get('pathtrace_gpu_p99_ms', ''),
                row.get('commit_resources_cpu_p50_ms', ''),
                row.get('frame_cpu_p50_ms', ''),
                row.get('wall_clock_ms', ''),
                row.get('rmse', ''),
            ]
            table_rows.append('<tr>' + ''.join(
                f'<td>{c if "<span" in str(c) else html.escape(str(c))}</td>'
                for c in cells) + '</tr>')

        table_html = f"""
            <div class="runs-table">
              <table>
                <thead><tr>
                  <th>timestamp</th><th>status</th><th>sha</th>
                  <th>PT p50 (ms)</th><th>PT p99 (ms)</th>
                  <th>commit p50 (ms)</th><th>frame cpu p50 (ms)</th>
                  <th>wall (ms)</th><th>rmse</th>
                </tr></thead>
                <tbody>{''.join(table_rows)}</tbody>
              </table>
            </div>"""

        fixture_blocks.append(
            f'<div class="fixture">'
            f'<div class="fixture-meta">'
            f'  {payload["rows"]} runs · latest pyxis '
            f'  <code>{html.escape(payload["latest_pyxis"][:60])}</code> · '
            f'  gpu <code>{html.escape(payload["latest_gpu"])}</code> · '
            f'  status <code>{html.escape(payload["latest_status"])}</code> · '
            f'  csv <code>{html.escape(payload["csv"])}</code>'
            f'</div>'
            f'<div class="charts">{"".join(charts)}</div>'
            f'{table_html}'
            f'</div>'
        )

    return (_HTML_TEMPLATE
            .replace('__GENERATED_AT__',
                     datetime.datetime.now(datetime.timezone.utc)
                             .replace(microsecond=0).isoformat())
            .replace('__FIXTURE_COUNT__', str(len(payloads)))
            .replace('__TABS__', tabs_html)
            .replace('__FIXTURES__', ''.join(fixture_blocks))
            .replace('__PAYLOAD__', json.dumps(payloads))
            .replace('__KPIS__', json.dumps(
                [{'column': c, 'label': l, 'unit': u}
                 for c, l, u in _TRACKED_KPIS])))


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument('--csv', type=Path, action='append', required=True,
                        help='Per-fixture KPI CSV. Pass once per fixture; tabs '
                             'in the rendered page collapse them.')
    parser.add_argument('--out', type=Path, required=True,
                        help='Output HTML path.')
    args = parser.parse_args(argv)

    payloads = []
    for csv_path in args.csv:
        if not csv_path.exists():
            print(f'ERROR: CSV not found at {csv_path}', file=sys.stderr)
            return 2
        try:
            payloads.append(_fixture_payload(csv_path))
        except (OSError, csv.Error) as exc:
            print(f'ERROR: failed to parse {csv_path}: {exc}', file=sys.stderr)
            return 2

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(_render(payloads), encoding='utf-8')
    print(f'wrote {args.out} ({args.out.stat().st_size:,} bytes, '
          f'{len(payloads)} fixture(s))')
    return 0


if __name__ == '__main__':
    sys.exit(main())
